<?php 

$number = 4;

//switch statement skeleton
switch ($number) {
	case 34:
		echo "is it 34?";
		break;
	case 4:
		echo "is it 4?";
		break;
	case 10:
		echo "is it 10?";
		break;
	
	default:
		echo "number doesn't exist";
		break;
}

 ?>
